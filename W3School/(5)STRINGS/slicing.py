b=" Hello, World "
print(b[2:5])
print(b[:5]) # starts from the string and ends at the 5th character
print(b[2:]) # Get the characters from position 2, and all the way to the end
print(b[-5:-2]) # ist 5 and last 2 characters will not be included
print(b.upper())
print(b.lower())
print(b.strip()) #The strip() method removes any whitespace from the beginning or the end
print(b.replace("H","J")) #replaces a string with another string
print(b.split(',')) #splits the string into substrings if it finds instances of the separator
