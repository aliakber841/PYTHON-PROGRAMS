a='ali'
print(a.capitalize()) #Ali
print(a.casefold()) #ali
print(a.count(a)) #1
print(a.endswith("l")) #False
print(a.find("i")) #2
print(a.isalnum()) #True
print(a.isalpha()) #True
print(a.isascii()) #True
print(a.isdecimal()) #False
print(a.isdigit()) #False
print(a.isnumeric()) #False
print(a.lower()) #ali
print(a.upper()) #ALI
print(a.split('l')) # ['a', 'i']
print(a.title()) #Ali
print(a.partition('a')) # ('', 'a', 'li')
